        <div class="col-lg-12 text-left">
        <h4 class="tituloItem"><a data-toggle="collapse" href="#FAQ1" role="button" aria-expanded="false" aria-controls="FAQ1">1.	O que é o SCPA?</a></h4>
        <div class="collapse" id="FAQ1">
          <p>O SCPA é o Sistema de Cadastro e Permissão de Acesso do Ministério da Saúde, desenvolvido pelo Departamento de Informática do SUS (DATASUS), com o intuito de unificar o acesso e conferir maior segurança a sistemas web do Ministério da Saúde.</p>
        </div>
        <br>
      </div>
      <div class="col-lg-12 text-left">
        <h4 class="tituloItem"><a data-toggle="collapse" href="#FAQ2" role="button" aria-expanded="false" aria-controls="FAQ2">2.	Como solicitar acesso ao e-SUS AF</a></h4>
        <div class="collapse" id="FAQ2">
          <p>A solicitação de acesso ao e-SUS AF pode ser realizada na tela de login do Sistema, clicando na opção “Novo por aqui?”. Para prosseguir com o cadastro, siga os passos do vídeo “Solicitação de Perfil”.</p>
        </div>
        <br>
      </div>
      <div class="col-lg-12 text-left">
        <h4 class="tituloItem"><a data-toggle="collapse" href="#FAQ3" role="button" aria-expanded="false" aria-controls="FAQ3">3.	Esqueci minha senha de acesso ao e-SUS AF, o que devo fazer?</a></h4>
        <div class="collapse" id="FAQ3">
          <p>Na tela de login do e-SUS AF, clique na opção “Esqueceu a senha?”. Na página seguinte, insira seu e-mail de acesso e clique no botão “Enviar”. Será enviado e-mail com link para alteração da senha de acesso.</p>
        </div>
        <br>
      </div>
      <div class="col-lg-12 text-left">
        <h4 class="tituloItem"><a data-toggle="collapse" href="#FAQ4" role="button" aria-expanded="false" aria-controls="FAQ4">4.	Mais de um operador pode utilizar o mesmo e-mail para acessar o e-SUS AF?</a></h4>
        <div class="collapse" id="FAQ4">
          <p>Não. O SCPA (Sistema de Cadastro e Permissão de Acesso) não permite cadastrar operadores diferentes utilizando o mesmo endereço de e-mail.</p>
        </div>
        <br>
      </div>
      <div class="col-lg-12 text-left">
        <h4 class="tituloItem"><a data-toggle="collapse" href="#FAQ5" role="button" aria-expanded="false" aria-controls="FAQ5">5.	Posso ter acesso a dois ou mais estabelecimentos de saúde no e-SUS AF?</a></h4>
        <div class="collapse" id="FAQ5">
          <p>Sim. Basta realizar novas solicitações de perfil com as características desejadas, como mostrado no vídeo “Solicitação de perfil”.</p>
        </div>
        <br>
      </div>
      <div class="col-lg-12 text-left">
        <h4 class="tituloItem"><a data-toggle="collapse" href="#FAQ6" role="button" aria-expanded="false" aria-controls="FAQ6">6.	Quem é o Gestor estadual no e-SUS AF?</a></h4>
        <div class="collapse" id="FAQ6">
          <p>O Gestor estadual no e-SUS AF é o profissional designado como responsável pela implantação do Sistema nos estabelecimentos de saúde estaduais. Este profissional é o ponto focal para contato com as equipes de suporte do e-SUS AF do Ministério da Saúde e para realização de interlocução com a Gestão Estadual, estando sob sua responsabilidade a disseminação de informações para os usuários do Sistema nessa esfera administrativa.</p>
        </div>
        <br>
      </div>
      <div class="col-lg-12 text-left">
        <h4 class="tituloItem"><a data-toggle="collapse" href="#FAQ7" role="button" aria-expanded="false" aria-controls="FAQ7">7.	Quem é o Gestor Municipal no e-SUS AF?</a></h4>
        <div class="collapse" id="FAQ7">
          <p>Gestor municipal no e-SUS AF é o profissional indicado como responsável pela implantação do Sistema nos estabelecimentos de saúde municipais. É com esse profissional que a equipe de suporte do e-SUS AF entra em contato. Atenção! O Gestor municipal do e-SUS AF não é, necessariamente, o Secretário Municipal de Saúde nem o Coordenador da Assistência Farmacêutica municipal.</p>
        </div>
        <br>
      </div>
      <div class="col-lg-12 text-left">
        <h4 class="tituloItem"><a data-toggle="collapse" href="#FAQ8" role="button" aria-expanded="false" aria-controls="FAQ8">8.	Como informar o cancelamento de um usuário no e-SUS AF?</a></h4>
        <div class="collapse" id="FAQ8">
          <p>O usuário deve entrar em contato com o gestor de sua esfera de atuação para solicitação de inativação de perfil.</p>
        </div>
        <br>
      </div>
      <div class="col-lg-12 text-left">
          <h4 class="tituloItem"><a data-toggle="collapse" href="#FAQ9" role="button" aria-expanded="false" aria-controls="FAQ9">9.	Como são realizados os cadastros de Estabelecimentos de Saúde no e-SUS AF?</a></h4>
          <div class="collapse" id="FAQ9">
            <p>O cadastro dos estabelecimentos de saúde no e-SUS AF é realizado por meio da pesquisa do número de CNES (Cadastro Nacional de Estabelecimentos de Saúde) ou CNPJ, na funcionalidade “Cadastro de Estabelecimento” (Módulo Administrativo). Os passos seguintes são descritos em detalhe no vídeo “Cadastro de Estabelecimento”. Atenção! Dois estabelecimentos de saúde não podem compartilhar o mesmo número de CNES ou CNPJ no e-SUS AF.</p>
          </div>
          <br>
        </div>
        <div class="col-lg-12 text-left">
          <h4 class="tituloItem"><a data-toggle="collapse" href="#FAQ10" role="button" aria-expanded="false" aria-controls="FAQ10">10.	Como identificar em qual Estabelecimento de Saúde você está vinculado para acessar o e-SUS AF?</a></h4>
          <div class="collapse" id="FAQ10">
            <p>Caso o usuário tenha mais de um perfil de acesso no e-SUS AF, os perfis disponíveis, esferas de gestão e estabelecimentos de saúde vinculados serão mostrados em tela, logo após o login no Sistema. Caso o operador possua somente um perfil, o estabelecimento de saúde vinculado poderá ser conferido no canto superior direito da tela, após clicar no nome do operador logado.</p>
          </div>
          <br>
        </div>
      </div>
      <div class="col-lg-6 text-left">

        <div class="col-lg-12 text-left">
          <h4 class="tituloItem"><a data-toggle="collapse" href="#FAQ11" role="button" aria-expanded="false" aria-controls="FAQ11">11.	Qual a diferença entre “Estabelecimento de Saúde”, “Departamento” e “Localização Física”?</a></h4>
          <div class="collapse" id="FAQ11">
            <p>No e-SUS AF, os “Estabelecimentos de Saúde” representam os locais onde os medicamentos e produtos para a saúde são armazenados ou dispensados aos usuários SUS, por exemplo: unidades de saúde, almoxarifados, centrais de abastecimento farmacêutico, hospitais etc. Cada usuário no Sistema deve estar vinculado a um estabelecimento de saúde para acessar as funcionalidades relacionadas à movimentação de produtos. Os “Departamentos” são os ambientes internos de um estabelecimento de saúde, que recebem produtos para uso na própria unidade, por exemplo: sala de curativo, sala de nebulização, sala de vacinas, centro cirúrgico etc. “Localização Física” é o local (endereço) onde os produtos são estocados nos estabelecimentos de saúde, tais como: estantes, armários, prateleiras, pallets, geladeira, câmara fria etc.</p>
          </div>
          <br>
        </div>
        <div class="col-lg-12 text-left">
          <h4 class="tituloItem"><a data-toggle="collapse" href="#FAQ12" role="button" aria-expanded="false" aria-controls="FAQ12">12.	Depois de realizar uma entrada em estoque de uma nota fiscal e salvá-la, essas informações podem ser alteradas?</a></h4>
          <div class="collapse" id="FAQ12">
            <p>Não. A partir do momento em que você armazena uma entrada, os produtos são incluídos em estoque. Portanto, o Sistema não permite alteração dos dados da nota fiscal (número, especificação dos medicamentos, quantidade, valor, etc.).</p>
          </div>
          <br>
        </div>
