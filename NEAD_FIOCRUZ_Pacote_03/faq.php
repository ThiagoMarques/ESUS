          <h4 class="tituloItem"><a data-toggle="collapse" href="#FAQ13" role="button" aria-expanded="false" aria-controls="FAQ13">13.	Posso registrar no e-SUS AF a distribuição de medicamentos para estabelecimentos que não utilizam o sistema?</a></h4>
          <div class="collapse" id="FAQ13">
            <p>Sim. O registro da distribuição de medicamentos para estabelecimentos de saúde que não utilizam o e-SUS AF é realizado por meio da funcionalidade “Requisição”, opção “Distribuição sem solicitação”. Maiores detalhes no vídeo de “Requisição”.</p>
          </div>
          <br>
        </div>
        <div class="col-lg-12 text-left">
          <h4 class="tituloItem"><a data-toggle="collapse" href="#FAQ14" role="button" aria-expanded="false" aria-controls="FAQ14">14.	O cadastro de usuário SUS precisa ser realizado para cada estado ou município no e-SUS AF?</a></h4>
          <div class="collapse" id="FAQ14">
            <p>Não. O e-SUS AF utiliza o CNS (Cartão Nacional de Saúde) para vinculação dos usuários SUS. Assim, uma vez inserido no Sistema, o cadastro de usuário pode ser acessado por todos os operadores do e-SUS AF, independentemente do estabelecimento vinculado e sua esfera de gestão.</p>
          </div>
          <br>
        </div>
        <div class="col-lg-12 text-left">
