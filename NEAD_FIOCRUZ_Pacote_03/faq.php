          <h4 class="tituloItem"><a data-toggle="collapse" href="#FAQ16" role="button" aria-expanded="false" aria-controls="FAQ16">16.	Como faço para retirar produtos vencidos do estoque no e-SUS AF?</a></h4>
          <div class="collapse" id="FAQ16">
            <p>Produtos com validade vencida são retirados do estoque do estabelecimento de saúde pela funcionalidade “Saídas Diversas”, Tipo de saída “Validade Vencida”. Para maiores informações, veja o vídeo “Saída Diversa”, disponível neste curso.</p>
