          <div class="collapse" id="FAQ15">
            <p>Sim. As dispensações no e-SUS AF podem ser realizadas com ou sem agendamento. Para maiores informações, veja o vídeo “Dispensação”, disponível neste curso.</p>
          </div>
          <br>
        </div>
